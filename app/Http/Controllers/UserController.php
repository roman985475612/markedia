    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $request->validate([
            'name' => 'required',
            'email' => 'required|email|unique:users',
            'password' => 'required|confirmed',
        ]);

        $user = User::create([
            'name' => $request->name,
            'email' => $request->email,
            'password' => Hash::make($request->password),
        ]);

        Auth::login($user);

        return redirect()->home()->with('success', 'Вы успешно зарегистрировались!');
    }

    public function login() 
    {
        return view('user.login');    
    }

    public function loginStore(Request $request)
    {
        $request->validate([
            'email' => 'required|email',
            'password' => 'required'
        ]);

        $credentials = $request->only('email', 'password');

        if (Auth::attempt($credentials)) {
            if (Auth::user()->is_admin) {
                return redirect()->route('admin.index')->with('seccess', 'Добрый день, администратор!');
            }
            return redirect()->home()->with('success', 'Добрый день, пользователь!');
        }
        return redirect()->route('login')->with('error', 'Неправельный логин или пароль');
    }

    public function logout()
    {
        Auth::logout();
        return redirect()->route('home');
    }

